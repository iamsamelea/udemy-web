// OBJETO MATH

let resultado;


/**Funciones más utilizadas de Math**/ 

resultado = Math.PI; // Da como resultado el número PI
resultado = Math.round(2.5); // Redondea el número de una forma normal
resultado = Math.ceil(2.1); // Redondea el resultado hacia arriba
resultado = Math.floor(2.9); // Redondea el resultado hacia abajo
resultado = Math.sqrt(144); // Da como resultado la raíz cuadrada del número
resultado = Math.abs(-200); // Cambia el resultado del número a positivo
resultado = Math.min(1, 5, 20, 3, 18, 23, 9); // Da como resultado el número más pequeño
resultado = Math.max(1, 5, 20, 3, 18, 23, 9); //Da como resultado el número más grande
resultado = Math.random(); // Da un resultado random siempre por debajo de 1


resultado = Math.floor(Math.random() * 30); // Con este tipo de función conseguimos un número random mayor que uno porque lo multiplicamos por 30

console.log(resultado);