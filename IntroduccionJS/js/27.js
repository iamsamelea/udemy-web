// Do While Loop
let i = 0; // Indice

do {
    console.log(i); // Código que se ejecuta

    i++;    // Incremento
} while ( i < 10); // Condición



//En el Do While Loop se ejecuta almenos el código 1 vez, y después evalua si el código se cumple.
//Paso 1: declaramos la variable que la inicamos en 0 ( Indice ).
//Paso 2: utilizamos la palabra reservada do y le abrimos unas llaves, que es donde se ejecutará el codigo.
